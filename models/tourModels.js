// virtual populate
mySchema.virtual('reviews', {
  ref: 'Review',
  foreignField: 'tour',
  localField: '_id',
});

//for referencing
mySchema.pre(/^find/, function (next) {
  this.populate({
    path: 'guides',
    select: '-__v',
  });
  next();
});
let Tour = new mongoose.model('Tour', mySchema);

module.exports = Tour;
